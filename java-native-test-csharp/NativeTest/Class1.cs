using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace NativeTest {
	/**
	 * 重复器。
	 * 加工传入的文本再返回。
	 */
	public class Repeater {

		public String helloWorldRepeat(String str) {
			return "HelloWorld: " + str;
		}

		public String hiRepeat(String str) {
			return "Hi: " + str;
		}

		public String describe() {
			return "this is a test";
		}
	}
}
